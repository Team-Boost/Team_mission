import collections, operator

def reduce(function, iterable, initializer=None):
  it = iter(iterable)
  if initializer is None: 
    value = next(it) 
  else: 
    value = initializer 
  for element in it: 
    value = function(value, element)
  return value

def merge_dict_1(dict_first,dict_second):
  concat_dict=[dict_first,dict_second]
  result = dict(reduce(operator.add,
         map(collections.Counter, concat_dict)))
  return result

dict_first={'사과':30 , '배' : 15, '감':10, '포도':10}
dict_second={'사과':5 , '감' : 25, '배':15, '귤':10}

merge_dict_1(dict_first,dict_second)

