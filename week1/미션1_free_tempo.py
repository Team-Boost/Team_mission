def merge_dict_2(dict_first,dict_second):
  merge_dict={}
  index=list(set(dict_first.keys())|set(dict_second.keys()))
  for fruit in index:
    if fruit in dict_first.keys():
      dict_first_value=dict_first[fruit]
    else:
      dict_first_value=0
    if fruit in dict_second.keys():
      dict_second_value=dict_second[fruit]
    else:
      dict_second_value=0
    merge_dict[fruit]=dict_first_value+dict_second_value
  return merge_dict

merge_dict_2(dict_first,dict_second)

